int ft_any(char **tab, int (*f)(char*))
{
	int		i;
	int		k;

	i = 0;
	while (tab[i] != 0)
	{
		k = 0;
		while (tab[i][k] != 0)
		{
			if (f(tab[i][k]) == 1)
				return (1);
			k++;
		}
		i++;
	}
	return (0);
}
