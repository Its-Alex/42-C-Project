int ft_find_next_prime(int nb)
{
	int i;

	i = 2;
	if (nb == 0 || nb == 1)
		return (2);
	while (nb >= i)
	{
		if (i == nb)
			return (nb);
		if (nb % i == 0)
		{
			nb++;
			i = 2;
		}
		i++;
	}
	return (0);
}
