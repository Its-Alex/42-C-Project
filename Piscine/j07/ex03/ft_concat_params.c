char	*ft_concat_params(int argc, char **argv)
{
	int i;
	int j;
	int k;

	while (argc > i)
	{
		argc[0][k] = argc[j][i];
		i++;
		k++;
		if (argv[i][j] == '\0')
		{
			argv[i][j] == '\n';
			j++;
		}

	}
}
