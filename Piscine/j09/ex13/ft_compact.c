int		ft_compact(char **tab, int lenght)
{
	int i;
	int k;

	i = 0;
	while (*tab[i] != '\0')
	{
		if (*tab[i] == 0)
		{
			k = 0;
			while (*tab[k + 1] != '\0')
			{
				*tab[k] = *tab[k + 1];
				k++;
			}
			*tab[k] = *tab[k + 1];
		}
		i++;
	}
	lenght = i;
	return (lenght);
}
