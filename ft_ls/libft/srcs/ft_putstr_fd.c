#include "../incs/libft.h"

void	ft_putstr_fd(const char *s, int fd)
{
	int i;

	if (s == NULL)
	{
		write(1, "(null)", 6);
		return ;
	}
	i = ft_strlen(s);
	write(1, s, i);
}
